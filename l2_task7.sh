#!/bin/bash

# 1. Використання stdout (1) для виведення інструкції
echo "--- Сценарій перевірки прав доступу ---"

# 2. Використання stdin (0) для зчитування даних від користувача 
echo -n "Введіть повний шлях до файлу, який хочете створити: "
read filepath

# 3. Спроба виконати команду та обробка коду завершення [cite: 213]
# Ми намагаємося створити файл. Потік stderr (2) від команди touch 
# перенаправляємо в /dev/null, щоб обробити помилку власноруч[cite: 215].
touch "$filepath" 2> /dev/null

if [ $? -eq 0 ] # Перевірка коду повернення останньої команди [cite: 203, 215]
then
    # Якщо код 0 — виводимо успіх у stdout [cite: 217]
    echo "Успіх: Файл '$filepath' створено."
    exit 0 # Власний код успішного завершення [cite: 226]
else
    # Якщо код не 0 — виводимо повідомлення у потік stderr (>&2) 
    echo "ПОМИЛКА: Не вдалося створити файл за шляхом '$filepath'." >&2
    exit 1 # Власний код помилки 
fi

